-- O comando SELECT em SQL é utilizado para consultar e recuperar dados de uma ou 
-- mais tabelas em um banco de dados.

-- Sua sintaxe básica é:
-- SELECT coluna1, coluna2 FROM nome_da_tabela WHERE condição;

-- Por exemplo, para selecionar todos os dados de uma tabela chamada clientes:
-- SELECT * FROM clientes;

-- O asterisco (*) indica que todas as colunas serão retornadas.

-- É possível filtrar resultados usando a cláusula WHERE, ordenar com ORDER BY, 
-- agrupar com GROUP BY, entre outras opções.
